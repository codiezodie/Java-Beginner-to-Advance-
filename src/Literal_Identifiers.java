public class Literal_Identifiers {
    public static void main(String[] args) {

        int age = 20;             // age is identifier, 20 is literal
        String name = "Zainab";   // name is identifier, "Zainab" is literal
        boolean isStudent = true; // isStudent is identifier, true is literal

        System.out.println(age);
        System.out.println(name);
        System.out.println(isStudent);
    }
}
